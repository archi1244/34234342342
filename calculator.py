def add(a, b):
    """Складывает два числа.

    Args:
        a (float): Первое число.
        b (float): Второе число.

    Returns:
        float: Сумма a и b.
    """
    return a + b

def subtract(a, b):
    """Вычитает из первого числа второе.

    Args:
        a (float): Первое число.
        b (float): Вычитаемое число.

    Returns:
        float: Разность a и b.
    """
    return a - b
